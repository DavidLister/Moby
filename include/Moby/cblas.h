inline void cblas_agemv(const enum CBLAS_ORDER order,
                 const enum CBLAS_TRANSPOSE TransA, const int M, const int N,
                 const mpfr::mpreal alpha, const mpfr::mpreal *A, const int lda,
                 const mpfr::mpreal *X, const int incX, const mpfr::mpreal beta,
                 mpfr::mpreal *Y, const int incY)
{
  char trans[] = "T";
  char ntrans[] = "N"; 
  if (TransA == CblasNoTrans)
    agemv_(ntrans, (int*) &M, (int*) &N, (mpfr::mpreal*) &alpha, (mpfr::mpreal*) A, (int*) &lda, (mpfr::mpreal*) X, (int*) &incX, (mpfr::mpreal*) &beta, Y, (int*) &incY, 1);
  else
    agemv_(trans, (int*) &M, (int*) &N, (mpfr::mpreal*) &alpha, (mpfr::mpreal*) A, (int*) &lda, (mpfr::mpreal*) X, (int*) &incX, (mpfr::mpreal*) &beta, Y, (int*) &incY, 1);
}

inline void cblas_agemm(const enum CBLAS_ORDER Order, const enum CBLAS_TRANSPOSE TransA,
                 const enum CBLAS_TRANSPOSE TransB, const int M, const int N,
                 const int K, const mpfr::mpreal alpha, const mpfr::mpreal *A,
                 const int lda, const mpfr::mpreal* B, const int ldb,
                 const mpfr::mpreal beta, mpfr::mpreal* C, const int ldc)
{
  char trans[] = "T";
  char ntrans[] = "N"; 
  if (TransA == CblasNoTrans)
  {
    if (TransB == CblasNoTrans)
      agemm_(ntrans, ntrans, (int*) &M, (int*) &N, (int*) &K, (mpfr::mpreal*) &alpha, (mpfr::mpreal*) A, (int*) &lda, (mpfr::mpreal*) B, (int*) &ldb, (mpfr::mpreal*) &beta, C, (int*) &ldc, 1, 1);
    else
      agemm_(ntrans, trans, (int*) &M, (int*) &N, (int*) &K, (mpfr::mpreal*) &alpha, (mpfr::mpreal*) A, (int*) &lda, (mpfr::mpreal*) B, (int*) &ldb, (mpfr::mpreal*) &beta, C, (int*) &ldc, 1, 1);
  }
  else
  {
    if (TransB == CblasNoTrans)
      agemm_(trans, ntrans, (int*) &M, (int*) &N, (int*) &K, (mpfr::mpreal*) &alpha, (mpfr::mpreal*) A, (int*) &lda, (mpfr::mpreal*) B, (int*) &ldb, (mpfr::mpreal*) &beta, C, (int*) &ldc, 1, 1);
    else
      agemm_(trans, trans, (int*) &M, (int*) &N, (int*) &K, (mpfr::mpreal*) &alpha, (mpfr::mpreal*) A, (int*) &lda, (mpfr::mpreal*) B, (int*) &ldb, (mpfr::mpreal*) &beta, C, (int*) &ldc, 1, 1);
  }
}

